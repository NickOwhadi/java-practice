package day42_;

public class Person {
    private String name;
    private int age;
    
    //getter and setter public for name
    
    public String getName() {
    	return name;
    }
    public void setName(String name) {
    	this.name=name;
    }
    
    
    public int getAge() {
    	return age;
    }
    public void setAge() {
    	this.age=age;
    }
    
}
